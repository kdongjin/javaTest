package javatest;

public class Add {

	//시작
	public static void main(String[] args) {
		//변수설정
		int 숫자1;
		int 숫자2;
		int 합계;
		
		//입력설정
		숫자1 = 10;
		숫자2 = 20; 
		
		//연산
		합계 = 숫자1 + 숫자2 ; 
		
		//출력
		System.out.println(숫자1 + "+" + 숫자2 +"=" +합계);
		System.out.println("숫자1 + 숫자2 =" +합계);
	}
	//종료

}
